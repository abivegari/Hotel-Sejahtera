    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        if (inputAlamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data Order GAGAL disimpan\nAlamat Customer harus diisi", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else if (inputNamaCust.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data Order GAGAL disimpan\nNama Customer harus diisi)", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else if (inputNoTelp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data Order GAGAL disimpan\nNo. Telp harus diisi)", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else if (listResDetail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data Reservasi GAGAL disimpan\nTidak ada yang dipilih", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else if (Integer.parseInt(txtJumBayar.getValue().toString()) == 0) {
            JOptionPane.showMessageDialog(this, "Data Order GAGAL disimpan\nMasukkan Jumlah Bayar terlebih dahulu", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else if (Float.parseFloat(txtJumKembalian.getValue().toString()) < 0) {
            JOptionPane.showMessageDialog(this, "Data Order GAGAL disimpan\nJumlah Kembalian harus minimal nol(0)", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else {
            boolean sama = false;
            for (Customer cxx : daoCustomer.getAllData()) {
                if (inputKtp.getText().equals(cxx.getNoKtpCustomer().toString())) {
                    sama = true;
                    break;
                } else {
                    sama = false;
                }
            };
            if (sama == false) {
                newCustomer = new Customer();
                newCustomer.setNoKtpCustomer(inputKtp.getText());
                newCustomer.setNamaCustomer(inputNamaCust.getText());
                newCustomer.setAlamatCustomer(inputAlamat.getText());
                newCustomer.setNoTelpCustomer(inputNoTelp.getText());
                newCustomer.setEmailCustomer(inputEmail.getText());
                newCustomer.setJenisKelamin(cbJenisKelamin.getSelectedItem().toString());
                daoCustomer.insertData(newCustomer);

            } else {
                newCustomer.setNoKtpCustomer(inputKtp.getText());
                newCustomer.setNamaCustomer(inputNamaCust.getText());
                newCustomer.setAlamatCustomer(inputAlamat.getText());
                newCustomer.setNoTelpCustomer(inputNoTelp.getText());
                newCustomer.setEmailCustomer(inputEmail.getText());
                newCustomer.setJenisKelamin(cbJenisKelamin.getSelectedItem().toString());
                daoCustomer.updateData(newCustomer);

            }
            newReservasi.setIdPegawai(AplikasiReservasi.pegawai);
            newReservasi.setTanggalReservasi(new Date());
            newReservasi.setNoKtpCustomer(newCustomer);
            newReservasi.setResDetailList(listResDetail);
            newReservasi.setTotal(grandTotal);
            daoResDetail.book(newReservasi);
            JOptionPane.showMessageDialog(this, "No. Reservasi anda adalah " + newReservasi.getNoReservasi().toString());
            Cancel();
            reset();
        }
    }//GEN-LAST:event_btnBookActionPerformed

    private void btnCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekActionPerformed
        Cancel();
        int cek = 0;
        for (Customer cxx : daoCustomer.getAllData()) {
            if (inputKtp.getText().equals(cxx.getNoKtpCustomer())) {
                cek = 1;
                inputNamaCust.setText(cxx.getNamaCustomer());
                inputAlamat.setText(cxx.getAlamatCustomer());
                inputEmail.setText(cxx.getEmailCustomer());
                inputNoTelp.setText(cxx.getNoTelpCustomer());
                cbJenisKelamin.setSelectedItem(cxx.getJenisKelamin());
                break;
            } else {
                cek = 0;
            }
        };
        if (inputKtp.getText().length() != 16) {
            JOptionPane.showMessageDialog(this, "No. KTP Harus 16 Digit");
        } else {
            if (cek == 1) {
                JOptionPane.showMessageDialog(this, "Data Customer Sudah Ada");
            } else {
                JOptionPane.showMessageDialog(this, "Data Customer Belum Ada");
            }
        }

    }//GEN-LAST:event_btnCekActionPerformed

