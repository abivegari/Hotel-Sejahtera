    private void addModelComboTipeKamar() {
        String[] model = new String[listRoom.size()];
        for (int i = 0; i < listRoom.size(); i++) {
            model[i] = listRoom.get(i).getJenisRoom();
        }
        dcbmTipeKamar = new DefaultComboBoxModel(model);
        cbTipeKamar.setModel(dcbmTipeKamar);
    }

    private void printOrder() {
        int i = 1;
        dtmResDetail.getDataVector().removeAllElements();
        if (listResDetail.isEmpty()) {
            dtmResDetail.addRow(new Object[7]);
        } else {
            for (ResDetail rd : listResDetail) {
                dtmResDetail.addRow(new Object[]{
                    i,
                    rd.getIdRoom().getJenisRoom(),
                    rd.getQuantity(),
                    sdf.format(rd.getTanggalCheckIn()),
                    sdf.format(rd.getTanggalCheckOut())
                });
                i++;
            }
        }
    }

    public void reset() {
        inputKtp.setText(null);
        inputNamaCust.setText("");
        inputEmail.setText("");
        inputNoTelp.setText("");
        inputAlamat.setText("");
        cbJenisKelamin.setSelectedIndex(0);
        tglCheckOut.setDate(null);
        spinJmlKamar.setValue(1);
        txtStock.setText("");
        newCustomer = new Customer();
        newReservasi = new Reservasi();
        newPegawai = new Pegawai();
        grandTotal = 0.0;
        subTotal = 0.0;
        txtSubTotal.setValue(subTotal);
        txtTotal.setValue(grandTotal);
        txtJumBayar.setValue(0);
        listRoom = daoRoom.getAllData();
        printOrder();
        addModelComboTipeKamar();
        cbTipeKamar.setSelectedIndex(0);
        txtIdPegawai.setText(AplikasiReservasi.pegawai.toString());
    }

    private void Cancel() {
        tglCheckOut.setDate(null);
        spinJmlKamar.setValue(1);
        txtStock.setText("");
        txtSubTotal.setText("0");
        grandTotal = 0.0;
        subTotal = 0.0;
        txtTotal.setText("0");
        listResDetail.clear();
        dtmResDetail.getDataVector().removeAllElements();
        printOrder();
        addModelComboTipeKamar();
    }
