    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if ((int) spinJmlKamar.getValue() == 0) {
            JOptionPane.showMessageDialog(this, "Kamar tidak tersedia", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                if (listResDetail.isEmpty() && tglCheckOut.getDate() != null) {
                    //for (ResDetail rd : listResDetail) {
                    listResDetail.add(new ResDetail(
                            Integer.parseInt(spinJmlKamar.getValue().toString()),
                            Double.parseDouble(txtHarga.getValue().toString()),
                            tglCheckIn.getDate(),
                            tglCheckOut.getDate(),
                            listRoom.get(cbTipeKamar.getSelectedIndex())));
                    printOrder();
                    long difference = (tglCheckOut.getDate().getTime() - tglCheckIn.getDate().getTime());
                    int diffInDays = (int) (difference / 1000 / 60 / 60 / 24);
                    subTotal = (Integer.parseInt(spinJmlKamar.getValue().toString())
                            * (Double.parseDouble(txtHarga.getValue().toString())
                            * diffInDays));
                    txtSubTotal.setValue(subTotal);
                    grandTotal += subTotal;
                    txtTotal.setValue(grandTotal);
                    //}
                } else if (listResDetail != null && tglCheckOut.getDate() != null) {
                    boolean cek = true;
                    for (ResDetail rd : listResDetail) {
                        if (rd.getIdRoom().getJenisRoom().equals(cbTipeKamar.getSelectedItem().toString())) {
                            cek = false;
                            break;
                        }
                    }
                    if (cek) {
                        listResDetail.add(new ResDetail(
                                Integer.parseInt(spinJmlKamar.getValue().toString()),
                                Double.parseDouble(txtHarga.getValue().toString()),
                                tglCheckIn.getDate(),
                                tglCheckOut.getDate(),
                                listRoom.get(cbTipeKamar.getSelectedIndex())));
                        printOrder();
                        long difference = (tglCheckOut.getDate().getTime() - tglCheckIn.getDate().getTime());
                        int diffInDays = (int) (difference / 1000 / 60 / 60 / 24);
                        subTotal = (Integer.parseInt(spinJmlKamar.getValue().toString())
                                * (Double.parseDouble(txtHarga.getValue().toString())
                                * diffInDays));
                        txtSubTotal.setValue(subTotal);
                        grandTotal += subTotal;
                        txtTotal.setValue(grandTotal);
                    } else {
                        JOptionPane.showMessageDialog(this, "Kamar sudah dipilih 2x");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Tanggal Belum Diisi");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

